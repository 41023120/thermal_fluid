# 伍、實驗結果與討論
st.header("伍、實驗結果與討論")
st.image("./imges/16.png", caption=" 最終數值紀錄")
st.write("LED量測實驗中溫度、熱阻及發光效率的變化現象與影響")
st.write("LED（發光二極體）的性能受到溫度的顯著影響。透過LED量測實驗，可以研究溫度對熱阻和發光效率的影響，並討論其變化現象與原因：")
st.write("""
1. 溫度對LED的影響
LED的工作溫度升高主要由以下幾方面影響：
•	能隙（Bandgap）縮減： 半導體材料的能隙隨溫度升高而縮小，導致光子能量降低，影響發光效率。
•	載流子復合效率下降： 高溫會增加非輻射復合（Non-Radiative Recombination）的概率，從而降低光輸出。
•	材料特性改變： 高溫會導致材料內部應力、結構變化或加速老化。

""")
st.write("""
2. 熱阻的變化與影響
熱阻（Thermal Resistance）是LED芯片與散熱結構之間的熱傳導性能指標，主要由兩部分構成：
•	結構熱阻： LED內部芯片與基板之間的熱阻。
•	散熱熱阻： 散熱器與周圍環境的熱阻。
熱阻的變化現象：
•	當LED芯片內部或散熱結構的熱阻過高，會導致熱量積聚，升高結溫（Junction Temperature）。
•	結溫升高使熱阻進一步增加，形成惡性循環。
熱阻的影響：
•	高熱阻會降低LED的可靠性，加速老化，導致壽命縮短。
•	發光效率（光通量/電功率）降低，特別是在高電流操作下表現更明顯。


""")
st.write("""
3. 發光效率的變化
現象：
•	光通量降低： 隨著溫度升高，LED的發光效率降低，表現為光通量減少。
•	熱輻射損失增加： 一部分能量以熱的形式損失而非轉化為光。
原因：
•	量子效率下降： 高溫增加非輻射復合，降低內部量子效率（IQE）。
•	光提取效率下降： 高溫可能導致封裝材料的折射率變化或光學性能降低，影響光提取效率（EQE）。


""")
st.write("""
4. 實驗中觀察到的變化
•	結溫與熱阻： 當驅動電流增大或散熱不良時，結溫顯著升高，導致熱阻曲線變陡。
•	發光效率曲線： 隨著結溫上升，發光效率呈現非線性下降。
•	壽命縮短： 長期高溫操作下，LED可能發生光衰減（Lumen Depreciation）。


""")
st.write("""
5. 改善措施
•	優化散熱設計： 使用高導熱材料或改善散熱器結構以降低熱阻。
•	控制工作溫度： 減少過高電流運行，或使用主動冷卻技術（如風冷或液冷）。
•	改進封裝技術： 選擇高穩定性的封裝材料，提高光提取效率和耐熱性。

""")
st.write("""
總結
溫度升高會顯著影響LED的熱阻和發光效率，導致性能下降和壽命縮短。在LED設計與應用中，控制溫度並優化散熱性能是提高LED可靠性和效率的關鍵。實驗的數據分析有助於深入理解這些影響，並提供改進設計的方向。
 

""")
